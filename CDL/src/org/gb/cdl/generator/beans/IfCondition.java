package org.gb.cdl.generator.beans;

public class IfCondition {
	
	private String expression;
	private String thenclause;
	private String elseclause;
	
	public String getExpression() {
		return expression;
	}
	public void setExpression(String expression) {
		this.expression = expression;
	}
	public String getThenclause() {
		return thenclause;
	}
	public void setThenclause(String thenclause) {
		this.thenclause = thenclause;
	}
	public String getElseclause() {
		return elseclause;
	}
	public void setElseclause(String elseclause) {
		this.elseclause = elseclause;
	}
	
	

}
